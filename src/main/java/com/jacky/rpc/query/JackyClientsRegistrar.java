    public void registerFeignClients(AnnotationMetadata metadata,
                                     BeanDefinitionRegistry registry) {

        ClassPathScanningCandidateComponentProvider scanner = getScanner();
        AnnotationTypeFilter annotationTypeFilter = new AnnotationTypeFilter(JackyQuery.class);
        scanner.addIncludeFilter(annotationTypeFilter);
        Set<String> basePackages = getBasePackages(metadata);

        Map<String, Object> attrs = metadata
                .getAnnotationAttributes(EnableJackyClient.class.getName());

        for (String basePackage : basePackages) {
            Set<BeanDefinition> candidateComponents = scanner.findCandidateComponents(basePackage);
            for (BeanDefinition beanDefinition : candidateComponents) {
                if (beanDefinition instanceof AnnotatedBeanDefinition) {
                    AnnotatedBeanDefinition annotatedBeanDefinition = (AnnotatedBeanDefinition) beanDefinition;
                    AnnotationMetadata annotationMetadata = annotatedBeanDefinition.getMetadata();
                    Map<String, Object> annotationAttributes = annotationMetadata.getAnnotationAttributes(JackyQuery.class.getCanonicalName());

                    registerFeignClient(registry,annotationMetadata,annotationAttributes);
                }
            }
        }
    }



